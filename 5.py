x = int(input('Введите число x'))
y = int(input('Введите число y'))
if x > 0 < y:
    print('первая четверть')
elif x < 0 < y:
    print('вторая четверть')
elif x < 0 > y:
    print('третья четверть') 
elif x > 0 > y  :     
    print('четвертая четверть')
else :
    print('точка на оси')    
